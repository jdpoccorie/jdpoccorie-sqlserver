-- EJEMPLO DE CAST
SELECT '123.45', CAST('123.45' AS DECIMAL(18,6));

SELECT '123.45' + 1; -- No siempre SQL logra realizar la conversión
SELECT CAST('123.45' AS DECIMAL(18,6)) + 1; -- PARA QUE SE CONSIGA LA CONVERSIÓN DEBE SER EXPLICITA

SELECT CAST('123.45' AS INT); -- ERROR, La parte decimal genera error
SELECT CAST('123.45' AS BIGINT); -- ERROR


-- FECHAS
SELECT CAST('2023-10-15 17:25' AS DATE)
SELECT CAST('2023-10-15T1725:45.1234' AS DATETIME) -- DATETIME SOPORTA HASTA 3 FRACCIONES DE SEGUNDO
SELECT CAST('2023-10-15T17:25:45.1234' AS DATETIME2) -- DATETIME2 SOPORTA HASTA 7 FRACCIONES DE SEGUNDO
													 -- EN NUEVOS PROYECTOS, SE RECOMIENDA DATETIME2 PARA MAS PRECISION
													 -- COMPLETA LA FRACCIÓN CON 0s

SELECT CAST('2023-10-15T17:25:45.1234567' AS DATETIME2)
SELECT CAST('2023-10-15T17:25:45.12345678' AS DATETIME2) -- SI SE EXCEDE LA CAPACIDAD DE DATETIME2, REDONDEA

